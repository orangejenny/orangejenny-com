            <h4>Defining the mobile need</h4>
            <p>Our team researched our target user group, parents of toddlers, in order to better understand where a mobile application could best serve them. We researched family and parenting blogs and also conducted informal interviews with parents of toddlers.</p>
            <p>In our research, we learned that parents of toddlers often grapple with not only coordinating meals and making meals that their children will actually eat, but parents are increasingly concerned about preparing meals that are sustainable and healthful. This entails buying foods that are in-season and are grown locally and organically, a task that is easier said than done. Farmers markets can be difficult to find, and they often have very restricted schedules.</p>
            <p>In summary, we determined that parents of toddlers have the following needs:</p>
                <li>Helping families locate farmer's markets and other sustainable and local food sources</li>
                <li>Find food that fulfills dietary restrictions such as diabetes and gluten-free</li>
                <li>Identify new foods to empower the food-shopping experience</li>
