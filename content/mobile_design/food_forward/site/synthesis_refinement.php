          <p>After generating a variety of ideas for different tasks and user   groups, we chose three of the strongest to elaborate, discussing how   they would and would not meet Susan's needs.</p>
            <li>Food sense: an application that would help Susan select food   for shopping and cooking. It would recommend nutritious and sustainable   choices. This application would also incorporate the specific food   preferences of Susan's family.</li>
            <li>Anxiety: an application to assist users in times of stress. We   discussed ways an application could help Susan calm down during   stressful work situations. The application could also assist users with   mild anxiety disorders or phobias.</li>
            <li>Explorer: an application that would enable Susan to find new   locations to spend time in her community and meet new people who share   her interests. We questioned, however, whether a busy parent of a   toddler is motivated to constantly wander her community.</li>
